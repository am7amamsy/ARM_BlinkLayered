LED_LevelType LED_ReadChannel(LED_ChannelType channelId)
{
    channelId = LED_CH;
    LED_LevelType level = Dio_ReadChannel(channelId);
    return level;
}

void LED_WriteChannel(LED_ChannelType channelId, LED_LevelType level)
{
    channelId = LED_CH;
    Dio_WriteChannel(channelId, level);
}
 
LED_LevelType LED_FlipChannel(LED_ChannelType channelId)
{
    channelId = LED_CH;
    LED_LevelType level = Dio_FlipChannel(channelId);
    return level;
}

