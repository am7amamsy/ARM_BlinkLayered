void LED_Blink()
{
    LED_LevelType level = LED_ReadChannel(LED_CH);
    //SysTick_StopTimer();
    if(level == LED_OFF)
    {
        LED_WriteChannel(LED_CH, LED_ON);
        SysTick_StartTimer(timeOn);
    }
    else if(level == LED_ON)
    {
        LED_WriteChannel(LED_CH, LED_OFF);
        SysTick_StartTimer(timeOff);
    }
}
 
